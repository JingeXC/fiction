angular.module('starter.directives',[])
  .directive('goLogin',function(){
    return {
      restrict:'EA',
      replace:false,
      controller:function($scope,$state){
        $scope.goLoginPage=function(){
          $state.go('login');
        }
      }
    }
  })
  .directive('goHome',function(){
    return {
      restrict:'EA',
      replace:false,
      controller:function($scope,$state){
        $scope.goHomePage=function(){
          $state.go('tab.dash');
        }
      }
    }
  })
  .directive('goBack',function(){
    return {
      restrict:'EA',
      replace:false,
      controller:function($scope,$state){
        $scope.goback=function(){
          window.history.back();
        }
      }
    }
  })
  .directive('goBookshelf',function(){
    return{
      restrict:'EA',
      replace:false,
      controller:function($scope,$state){
        $scope.goBookShelf=function(){
          $state.go('tab.bookShelf');
        }
      }
    }
  })
  .directive('goLibrary',function(){
    return{
      restrict:'EA',
      replace:false,
      controller:function($scope,$state){
        $scope.goLibrary=function(){
          $state.go('tab.chats');
        }
      }
    }
  })
  .directive('goRecharge',function(){
    return{
      restrict:'EA',
      replace:false,
      controller:function($scope,$state){
        $scope.goRecharge=function(){
          $state.go('tab.account');
        }
      }
    }
  })
